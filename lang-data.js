function changeLanguage(language) {
  // Cambia le traduzioni generali
  document.querySelectorAll('[data-key]').forEach((element) => {
    const key = element.getAttribute('data-key');
    if (langData[language] && langData[language][key]) {
      element.innerHTML = langData[language][key];
    }
  });

  // Cambia il contenuto del menu
  const menuContent = document.querySelector("#menu");
  if (menuContent) {
    menuContent.innerHTML = ''; // Pulisce il contenuto attuale del menu
    Object.keys(menuData[language]).forEach((section) => {
      const sectionElement = document.createElement("div");
      sectionElement.classList.add("menu-section");
      const sectionTitle = document.createElement("h3");
      sectionTitle.innerHTML = section.charAt(0).toUpperCase() + section.slice(1);
      sectionElement.appendChild(sectionTitle);

      menuData[language][section].forEach((dish) => {
        const dishElement = document.createElement("div");
        dishElement.classList.add("menu-item");
        dishElement.innerHTML = `
          <h4>${dish.nome}</h4>
          <p>${dish.descrizione}</p>
          <span>${dish.prezzo}</span>
        `;
        sectionElement.appendChild(dishElement);
      });

      menuContent.appendChild(sectionElement);
    });
  }
}

// Aggiungi un listener per il cambio lingua
document.getElementById("lang-switcher").addEventListener("change", function (event) {
  const selectedLang = event.target.value;
  changeLanguage(selectedLang);
});

// Carica la lingua predefinita al caricamento della pagina
window.onload = function () {
  const defaultLang = document.getElementById("lang-switcher").value;
  changeLanguage(defaultLang);
};

$(document).ready(function() {
  $(".menu-item").click(function() {
    var target = $(this).data("target");
    $(target).collapse('toggle');
  });
});
